    The three pieces that comprise <i>Still Sleeping</i> were recorded in August 2014 using simple generative procedures programmed in Arduino and interfaced to a modular synthesizer.

    While machine music is often associated with a hard, unfeeling energy, <i>Still Sleeping</i> explores its soft, fuzzy underbelly through a combination of 4-part Euclidean rhythms, gentle cycling in the Lydian mode, and the immodest application of Eventide Shimmer. Music for daydreaming.

