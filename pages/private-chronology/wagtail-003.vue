<p>
The two pieces that comprise <i>Days Gone By</i> pay tribute the portion of my life I've spent connecting with the 'American Primitive', and represent my most song-oriented guitar work to date.
<!--
With the combined encouragement from Ashley Paul and Glenn Jones, whom I'd spent the previous months recording in my living room, I made the decision to further complicate my scattershot discography with this pair of distinctly melancholic 'bedroom guitar' portraits.
</p>
<p>
-->
However, these pieces aren't without their own idiosyncracies. '02.01.2011' was improvised with the guitar laying on my lap, as if a slide guitar, but still fretted by my left hand and fingerpicked with my right, which lends lends the playing an uneven and lilting quality. '05.06.2010' was improvised nearly a year prior, around the same time as <a href="http://private-chronology.com/sensualsquare.html">Sensual Square</a>, but subsequently used to activate the artificial 'interior space' of a tape machine. The original recording was dubbed onto mono 1/4" tape with internal feedback (yielding a traditional tape echo), the result of which was then recursively subjected to the same procedure over a total of thirteen iterations. The result stops short of the complete acoustic transformations obtained by Alvin Lucier decades earlier, and instead, the final two iterations were mixed back in with the original recording to lend a ghostly reverberance to the brooding quality of the piece.
The tape component to this piece is available below, with the two last iterations panned hard left/right.
