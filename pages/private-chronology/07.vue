<p>
<i>Asleep at the Drawing Board</i> was recorded over a handful of playful sessions on a modular synthesizer in the spring of 2011. As with its sibling 7", <a href="http://private-chronology.com/days-gone-by.html">Days Gone By</a>, <i>Asleep at the Drawing Board</i> focuses on live instrumental performance, as opposed to Reuben's more process-based music from 2009-2011 with tape feedback systems. These recordings have been picked over and edited to yield a five-part suite of patchcable synthesis, flowing from a lumbering 'West Coast' spray of mis-firing oscillations and synthesized bog waters to auto-melodic studies complete with a murmuration of electric guitar chiming to bring it all to a bittersweet conclusion.</p>
