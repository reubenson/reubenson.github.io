<p>
<a href="http://blanksound.org">Caroline Park</a> and <a href="http://ashatamirisa.net">Asha Tamirisa</a>'s debut, <i>SOTO</i>, takes its material and title from their debut performance together at <a href="http://family-vineyard.com/artists/jed-speare">Jed Speare's</a> Studio Soto here in Boston. Seamlessly built upon a combination of generative patches, pre-recorded sound, and live-sampling, <i>SOTO</i> moves slowly as repeating motifs and percussive detail ebb effortlessly in and out of swells of ambient drone-sound.
<!--
There's a heavy, yet effortless quality to these recordings, and hearing it performed live evoked the sense that the sounds were emanating from it had a way of sounding unified with and indistinct from the room itself, similar in effect to the work of Jakob Ullmann.
-->
This cassette presents a two-part studio analogue of their performance, condensed and edited with care from the original board recording.
</p>
<p>
One-time edition of 100 copies duplicated on chrome tape in clear cassette shells imprinted by National Audio Company. The four-panel jcard features imagery by the artists and a still from a video taken during the performance and has been printed via a combination of offset printing, hand-stamping, and embossing on ivory, linen-finish paper.
</p>
