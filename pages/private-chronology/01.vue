This debut release features three pieces on guitar from erstwhile Fahey devotee Reuben Son. Two of the pieces herein focus heavily on usage of cassette tape as a physical medium, wrought to interact with the guitar in both the audio and the physical domain. Utilizing fingerpicking and tabletop guitar methodologies alongside variations on Riley's time lag accumulator, Reich's phase-shifting, and Stephen Cornford's<i> For Violin, Viola and Tape,</i> Son creates guitar-based music saturated with drone and decay.
<br> <br>
Hand-assembled edition of 200 by the artist, with labels scrawled onto painted cassette shells by fingerpick.

